#list append
l1=['a','b','c','d']
l1.append('r')
print(l1)
#list insert
l2=['1','2','3','4']
l2.insert(4,'5')
print(l2)
#Extend
l1.extend(l2)
print(l1)
l1.remove('d')
print(l1)
#pop
l1.pop()
print(l1)
#clear
l1.clear()
print(l1)
#sort
l3=[9,5,4,2,22,3]
l3.sort()
print(l3)
#reverse
l4=['akash','raju','yakub','sai']
l4.reverse()
print(l4)
